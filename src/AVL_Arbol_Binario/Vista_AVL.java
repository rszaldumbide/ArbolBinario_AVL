    private void btnGenerarRandomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarRandomActionPerformed
        // TODO add your handling code here:
        try {
            avl = new avl();
            arbol = this.pnlGrafico.getGraphics();

            int cantidad = Integer.parseInt(txtCantidad.getText());
            int numero;
            for (int i = 0; i < cantidad; i++) {
                //genera nums aleatorios desde el -10 al 20
                numero = (int) (Math.random() * 99 + 50);
                avl.insertar(numero);
            }
            graficarArbol(arbol, avl.getRaiz(), x, y, x2, y2, 1);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        txtCantidad.setText("");
    }//GEN-LAST:event_btnGenerarRandomActionPerformed

